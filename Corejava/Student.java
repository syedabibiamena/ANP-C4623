package corejava1;

class Student 
{

		 private int Id;
		 private String student_name;
		 private int age;
		public int getId() {
			return Id;
		}
		public void setId(int id) {
			Id = id;
		}
		public String getStudent_name() {
			return student_name;
		}
		public void setStudent_name(String student_name) {
			this.student_name = student_name;
		}
		public int getAge() {
			return age;
		}
		public void setAge(int age) {
			this.age = age;
		}
}
